% =========================================================================
% README: Code and Data Repository
% =========================================================================
%
% This repository contains the MATLAB scripts and processed data required 
% to reproduce the analyses and figures presented in the manuscript.
%
% -------------------------------------------------------------------------
% 1. Data Availability
% -------------------------------------------------------------------------
% Due to the excessively large storage size of the raw data, only the 
% pre-analyzed (processed) data necessary to generate the figures and 
% statistics are stored in this repository. The raw data can be provided 
% or uploaded upon request.
%
% -------------------------------------------------------------------------
% 2. Figure Generation
% -------------------------------------------------------------------------
% Each data-driven figure in the manuscript is accompanied by its specific 
% MATLAB plotting script to ensure full reproducibility.
% 
% Please note the exceptions:
% - MATLAB scripts are not provided for Figures 1a-c, 2a, 3a, 4a, 4c, 5a, 
%   and 5b, as these panels were not generated via MATLAB programming 
%   (e.g., they are conceptual diagrams, experimental paradigms, or 
%   illustrations created using other vector graphics software).
%
% -------------------------------------------------------------------------
% 3. Custom Functions & Core Analyses
% -------------------------------------------------------------------------
% - `Get_Mahalanobis_Distance.m`: This script contains the core algorithm 
%   used to calculate the Mahalanobis distance in Experiment 2.
%
% - `Plot_Time_Course`: A custom-built MATLAB tool used to visualize the 
%   time course results presented in Figures 3f, 3g, and 4d.
%
% =========================================================================